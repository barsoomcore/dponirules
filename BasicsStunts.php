<h1>STUNTS</h1>

<p>Imaginative players can come up with ways for their characters to use their skills in varied situations. The Stunt rule provides a basic guideline for adjudicating such uses, and to encourage players to be imaginative and creative in their combat choices.</p>

<p>When performing a stunt, characters can receive a bonus to one of their checks in a round by using one of their skills. They do not make a check with that skill; the bonus received is equal to the total bonus of the skill they are stunting with (including ranks, ability modifiers and any miscellaneous bonuses) divided by 5, rounded up. A character must have at least one rank in a skill to use it for stunting.</p>

<p class="notation">Stunt Bonus = (Skill Bonus / 5) Round UP</p>

<p>The player must describe (in a more or less convincing manner) how the skill is being used in order to receive the stunt bonus. A stunt bonus may be conceived of and applied AFTER the results of the check are known. The GM is the final arbiter of stunts and may disallow any suggested stunt at her discretion. The stunt bonus may be applied to <a href="Skills.php">Skill Checks</a>, <a href="BasicsAbilities.php">Ability Checks</a>, <a href="Combat.php#AttackBonus">Attack Rolls</a>, or <a href="Combat.php#ManeuverBonus">Combat Maneuver Rolls</a>. </p>

<p>Note that you may not apply stunt bonuses to <a href="BasicsSavingThrows.php">Saving Throws</a> or any sort of Recovery check (whether for <a href="CombatDamage.php">Damage</a> or <a href="BasicsConviction.php">Conviction</a>).</p>

<h2>Restriction</h2>
<p>Each time a skill is used for a stunt bonus in <a href="BasicsTime.php">a scene</a>, that skill's stunt bonus is reduced by one the next time it is used.</p>

