<h1>WEALTH</h1>

<p>Every character has a Wealth bonus that reflects his or her buying power &mdash; a representation of their hoard, their loot, even their booty. A character's Wealth bonus serves as the basis of the character's Wealth check, which is how characters get stuff.</p>

<h2>Wealth Bonus</h2>
<p>A character's starting Wealth bonus is equal to 5 + their Charisma score, increasing by +4 every time they take the <a href="Feats.php?Name=Wealthy">Wealthy</a> feat. Over the course of play, the hero's Wealth bonus will decrease as the hero purchases expensive items and increase as the hero gains levels.</p>
<p>A character's Wealth bonus can never fall below +0, and there is no limit to how high the Wealth bonus can climb.</p>
<p>Since Wealth is an abstract concept, it's sometimes difficult to determine how financially well off a character is. To get a general sense of how financially solvent a character is at any given time, check the table below.</p>

