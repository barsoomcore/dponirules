<h2>Making Purchases</h2>
<p>Wealth checks are used to determine what characters can afford and what stuff they might reasonably have access to. Every object and service has a cost, which is the Wealth check Difficulty; the higher the cost, the more expensive the item. To purchase an object, make a Wealth check against the difficulty.</p>

