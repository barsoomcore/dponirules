<h3>Wealth Awards</h3>
<p>Adventuring may result in characters finding valuable items (See: loot. Also: booty). In such cases, the benefit translates into a Wealth award.</p>

<h2>Equipping Your Character</h2>
<p>When buying your hero's starting equipment, you may wish to follow 
these basic guidelines to simplify matters. List all the equipment you 
want your hero to have. Purchase all the items that won't reduce your 
hero's Wealth score. Then purchase the remaining items, starting with 
the most expensive (highest purchase Difficulty) to the least expensive. 
This ensures your hero's Wealth will go as far as possible. </p>

<h2>Selling Stuff</h2>
<p>To sell something, a character first needs to determine its sale value. Assuming the object is undamaged and in working condition, the sale value is equal to the object's purchase difficulty (as if purchased new) minus 3.</p>
<p>Selling an object can provide an increase to a character's Wealth bonus. The increase is the same amount as the Wealth bonus loss the character would experience if the character purchased an object with a purchase difficulty equal to the sale value. </p>
<p>Regardless of the character's current Wealth bonus, he or she gains a Wealth bonus increase of 1 whenever the character sells an object with a sale value of 15 or higher. If A character sells an object with a sale value less than or equal to his or her current Wealth bonus, and that sale value is 14 or lower, the character gains nothing.</p>
<p>Obviously selling stuff you're not supposed to have is a bad idea, generally speaking.</p>

