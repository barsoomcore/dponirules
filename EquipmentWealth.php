<h2>The Wealth Check</h2>
<p>A Wealth check is a 1d20 roll plus your current Wealth bonus. The Wealth bonus is fluid. It increases as you gain Wealth and decreases as you make purchases.</p>
<p>If you succeed on the Wealth check, you gain the object. If you fail, you can't afford the object at the time.</p>
<p>If your current Wealth bonus is equal to or greater than the Wealth check difficulty, you automatically succeed.</p>
<p>If you successfully purchases an object or service with a Wealth check difficulty that's higher than your current Wealth bonus, your Wealth bonus decreases. </p>

<h2>Shopping and Time</h2>
<p>Buying less common objects generally takes a number of hours equal to the purchase difficulty of the object or service, reflecting the time needed to locate the wanted materials and close the deal. Getting a license or buying an object that is illegal in your current region increases the time needed to make purchases.</p>

<h2>Taking 10 and Taking 20</h2>
<p>You can usually take 10 or take 20 when making a Wealth check. Taking 20 requires 20 times as long as normal (you're shopping around for the best price).</p>
<p>Also, there is a penalty for spending beyond your means. Whenever you buy an object that has a purchase difficulty higher than your current Wealth bonus, your Wealth bonus decreases (see below).</p>

<h2>Try Again?</h2>
<p>You can try again if you fail a Wealth check, but not until you have spent an additional number of hours shopping equal to the purchase difficulty of the object or service. </p>

<h2>Aid Another</h2>
<p>You can make an aid another attempt to help a character purchase an object or service. If the attempt is successful, you provide the purchaser with a +2 bonus on his or her Wealth check.  If you aid a Wealth check for an item with a purchase difficulty higher than your current Wealth bonus, it decreases as normal.</p>

<h2>Losing Wealth</h2>
<p>Any time you purchase an object or service with a purchase difficulty higher than your current Wealth bonus, or one with a purchase difficulty of 15 or higher, your Wealth bonus goes down. How much the Wealth bonus is reduced depends on how expensive the object is. Your Wealth score is only reduced if the Wealth check succeeds; if the Wealth check fails, your Wealth score is unaffected.</p>

