<h1>DINO-PIRATES OF NINJA ISLAND</h1>

<h2>The SRD</h2>

<p>Here it is! The complete rules to the greatest game of pulp adventure ever created! <b>DINO-PIRATES OF NINJA ISLAND</b> is a swashbuckling role-playing game based on the very fine <a href="http://www.true20.com">True20 system</a> created and published by <a href="http://www.greenronin.com">Green Ronin</a> and you should TOTALLY buy a copy right now.</p>

<p>Here you'll find all the rules you need to create characters and run adventures in the world of DINO-PIRATES OF NINJA ISLAND. These rules are complete and not missing anything you'll need -- except for the setting itself, which isn't detailed here at all. So while there'll be references to ninjas, dinosaurs and pirates throughout, you'll find a lack of info actually telling you how all these various groups and factions get along (or even what they are).</p>

<p>But fear not because we will soon be providing a great deal of setting material online in a different format. In the meantime, why not purchase the original DINO-PIRATES OF NINJA ISLAND adventure, <a href="http://www.yourgamesnow.com/index.php?main_page=product_info&products_id=2364">SLAVE QUEEN OF THE RUINED CITY</a>? That'll be more than enough to get you started.</p>

<p>If you do buy SLAVE QUEEN OF THE RUINED CITY, you'll notice the sample characters there use the standard True20 rules, not the DINO-PIRATES OF NINJA ISLAND rules you see here. No worries! We've provided you with <a href="/dponiwiki/media/images/SQotRCDPoNIChars.pdf">a download of the characters, properly set up for these rules</a>. It's okay, you don't have to thank us. Just doing our job. The rest of the adventure ought to run just fine without any need for tweaking.</p>

