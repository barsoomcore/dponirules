<h1>FATIGUE</h1>
<p>Characters can suffer from fatigue as well as damage. This usually results from tasks requiring great effort, such as moving all out, exertion in difficult environments, and using <a href="Powers.php">powers</a>. There are four fatigue levels: <a href="PlayingConditions.php?Name=Strained">strained</a>, <a href="PlayingConditions.php?Name=Winded">winded</a>, <a href="PlayingConditions.php?Name=Fatigued">fatigued</a>, and <a href="PlayingConditions.php?Name=Exhausted">exhausted</a>.</p>

<h3>Strained</h3>
<p>A strained character suffers no ill effects, but is more susceptible to further fatigue. Like all fatigue conditions, a character must make a Recovery check to remove a Strained condition.</p>

<h3>Winded</h3>
<p>The character suffers a -1 penalty to effective Strength and Dexterity, and cannot move all out or charge. A winded character who suffers an additional fatigue result becomes fatigued.</p>

<h3>Fatigued</h3>
<p>The character cannot move all out or charge, and suffers a -2 penalty to effective Strength and Dexterity. A fatigued character who suffers an additional fatigue result becomes exhausted.</p>

<h3>Exhausted</h3>
<p>The character is near collapse. Exhausted characters move at half normal speed and suffer a -3 penalty to effective Strength and Dexterity. An exhausted character who suffers an additional fatigue result falls unconscious (and must recover from it normally before recovering from fatigue; see Damage Conditions).</p>

<h2>Recovery</h2>
<p>Heroes can attempt to recover fatigue after each dramatic scene (just like they can <a href="BasicsConviction.php#Recovery">recover Conviction</a>). Heroes who have only a Strained fatigue level recover automatically. To recover from more severe levels of fatigue, a hero makes a Constitution check (Difficulty 10) to recover from fatigue. Success reduces the character's fatigue condition by one level (from exhausted to fatigued, from fatigued to winded, winded to normal).</p>

<p>At the Narrator's discretion, long breaks may automatically recover all fatigue.</p>

