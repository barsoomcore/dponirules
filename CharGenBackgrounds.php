<h1>BACKGROUNDS</h1>
<p>Heroes in DINO-PIRATES OF NINJA ISLAND generally fall into one of four categories, each of which is represented by a particular Background: Ninjas, Dino-Pirates, Natives or Imperials.</p>
<p>A Background provides your character with two Bonus <a href="Feats.php">Feats</a>, two Bonus <a href="Skills.php">Skills</a> and two Favoured <a href="Feats.php">Feats</a>. The Bonus <a href="Feats.php">Feats</a> and <a href="Skills.php">Skills</a> are freebies for taking the Background; they are in addition to all normal feats and skills your character receives for <a href="Roles.php">Role</a> and Intelligence. The Favoured <a href="Feats.php">Feats</a> are feats drawn from a particular <a href="Roles.php">Role's</a> restricted list of feats. Any character with a given Background, no matter what their <a href="Roles.php">Role</a>, may take these <a href="Feats.php">feats</a> if they qualify for them in any other respect. That is, a <a href="Feats.php">feats</a> restricted to <a href="RolesAdept.php">Adepts</a> can be taken by a character who has that <a href="Feats.php">feats</a> on their list of Favoured <a href="Feats.php">Feats</a>, even if they are not taking a level of <a href="RolesAdept.php">Adept</a>. If the <a href="Feats.php">feats</a> has another requirement (minimum Ability score, for example), those requirements must be met in order for the character to take the <a href="Feats.php">feats</a>.</p>

