<p><b>Challenges:</b> You can take the following challenges with Control Shape:</p>

<p><i>Aggressive Change:</i> You can change shape and maintain your ability to engage in combat the same round. By increasing the Difficulty by 5, you can change shape as a move action instead of a standard action.

<p><i>Partial Change:</i> You can change only part of your body rather than transforming completely, for example, to grow claws or teeth for combat without having to assume hybrid or animal form. ON a success, you can attack with whichever natural weapons you would normally be able to use in your other forms without damaging your clothing or armor. The Difficulty is equal to that of assuming hybrid form, plus 5 (Difficulty 20). A partial change lasts until you decide to return to human form, and you must succeed on a Control Shape check to do so (Difficulty 20 or 25, depending on the phase of the moon).</p>

<p><b>Try Again (Voluntary Change):</b> A character can retry voluntary changes to animal form or hybrid form as often as he likes. Each attempt is a <a href="CombatActions.php?Time=Standard">standard action</a>. However, on a failed check to return to humanoid form, the character must remain in animal or hybrid form until the next dawn, when he automatically returns to humanoid form.</p>

<p><b>Special:</b> An afflicted lycanthrope cannot attempt a voluntary change until it becomes aware of its condition.</p>

