<h3>Control Shape</h3>
<h5 style="margin-top:0.1em;">(Wis)</h5>
<p>This skill determines whether an afflicted lycanthrope can control his shape. Any character who has contracted lycanthropy and is aware of his condition can learn this skill or use it untrained. A character who is not yet aware of his condition cannot use this skill. An adept or generalist can use <a href="Powers.php?Name=Body%20Control">Body Control</a> in place of the Control Shape skill.</p>

<p><b>Check (Involuntary Change):</b> An afflicted character must on a Difficulty 25 Control Change check at moonrise each night of the full moon to resist involuntarily assuming animal form. An injured character must also check for an involuntary change when injured: Difficulty 20 for <a href="PlayingConditions.php?Name=Hurt">hurt</a> and Difficulty 25 for <a href="PlayingConditions.php?Name=Wounded">wounded</a>.</p>

<p>On a failed check, the character must remain in animal form until the next dawn, when he automatically returns to his base form. A character aware of his condition may make one attempt to return to humanoid form (see below), but if he fails, he remains in animal form until the next dawn.</p>

<p><b>Try Again (Involuntary Change):</b> Check to resist an involuntary change once each time a triggering event occurs.</p>

<p><b>Check (Voluntary Change):</b> In addition, an afflicted lycanthrope aware of his condition may attempt to use this skill voluntarily in order to change to animal form, assume hybrid form, or return to humanoid form, regardless of the state of the moon or whether he has been injured.</p>

