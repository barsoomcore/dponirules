<h1>THE RULES</h1>
<p>These pages outline the rules of the role-playing game, DINO-PIRATES OF NINJA ISLAND.</p>

<h2>Core System</h2>
<p>DINO-PIRATES OF NINJA ISLAND uses a simple core mechanic to resolve actions. Whenever your character attempts any action with a chance of failure, do the following: </p>
<ol>
	<li>Roll a twenty-sided die (abbreviated d20).</li>
	<li>Add any relevant modifiers (for things like <a href="BasicsAbilities.php">abilities</a>, <a href="Skills.php">skills</a>, and circumstances). </li>
	<li>Compare the total to a number called the action's Difficulty (set by the Narrator based on the circumstances). </li>
</ol>
<p>If the result equals or exceeds the Difficulty Class (or "DC", for short), the action succeeds. If the result is lower than the Difficulty, the action fails. This simple system is used for nearly everything in DPoNI, with variations based on the modifiers added to a roll, the Difficulty, and the effects of success and failure.</p>

<p>There are three primary types of rolls that all use this mechanic: <a href="BasicsChecks.php">checks</a>, <a href="BasicsCombat.php">attack rolls</a> and <a href="BasicsSavingThrows.php">saving throws</a>.</p>



