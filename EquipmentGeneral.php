<h5>Legal Charges</h5>
<p>Characters get into trouble, and even in the lawless sorts of places where Dino-Pirates hang out, they can run afoul of the law. Fortunately, in most places you can pay your way out of trouble. These prices list some baseline costs for getting out of trouble, and represent fines, fees, and the sorts of bribes that are required to get someone to look the other way. A character who fails to make the requisite <a href="EquipmentWealth.php">Wealth</a> check will suffer the sorts of consequences the local justice system imposes.</p>
<p>Theft: You took something that wasn't yours, or caused property damage, or something like that, but at least nobody got hurt.</p>
<p>Violent crime not killing anyone: Bruises, injuries, and general unpleasantness, but nobody got dead.</p>
<p>Killed somebody who matters: This wasn't just some tavern brawl: somebody important died, and somebody's going to have to pay.</p>

<h5>Bribery</h5>
<p>It's really just the cost of doing business. These costs outline some ballpark figures for when a player wants to get someone to do something they wouldn't ordinarily wish to do. Not making the <a href="EquipmentWealth.php">Wealth</a> check imposes a -5 on any <a href="Skills.php?Name=Bluff">Bluff</a> or <a href="Skills.php?Name=Diplomacy">Diplomacy</a> checks made against the target.</p>

