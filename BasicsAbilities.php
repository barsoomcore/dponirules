<h1>ABILITY SCORES</h1>

<p>Characters have six abilities: Strength, Dexterity, Constitution, Intelligence, Wisdom, and Charisma. Strength, Dexterity, and Constitution are called physical abilities, whereas Intelligence, Wisdom, and Charisma are mental abilities. Each above-average ability score provides a bonus on certain die rolls; below average abilities apply a penalty to some die rolls.</p>

<h2>Choosing Ability Scores</h2>
<p>Ability scores have a numerical value, expressed as a penalty or bonus, from -5 (abysmal or disabled) to +5 or more (legendary, practically superhuman), with an average of +0. When ability scores are noted in this book, the score follows the ability's name, for example: Strength +2, Dexterity -1, and so forth.</p>
<p>You have 6 points to divide among your hero's abilities, which all start at 0, neither a bonus nor a penalty. This means you can have +1 in all six abilities; +3 in one ability, 0 in two others, and +1 in the remaining three; or any combination adding up to 6. The only limitation is you cannot put more than 5 points in a single ability score. (Scores can only go higher than +5 as a result of level advancement and a few other factors.)</p>

<p class="notation">Ability Scores = 6 points divided between six abilities.</p>

<p>If you choose to have a negative value in an ability, you gain bonus points to assign to your other ability scores. For example, if you give your hero Strength -1, you have 1 more point to assign to another ability (such as Intelligence). If your hero has Strength -2, you have 2 bonus points, and so on. Heroes cannot have abilities lower than -5, and abilities lower than -2 aren't recommended unless the hero is seriously deficient in that ability. Even with bonus points, you cannot put more than 5 points in a single ability score.</p>

<p>Your ability score is added to or subtracted from die rolls when you do something related to that ability. For example, your Strength score affects the amount of melee damage you do, your Intelligence score affects your Knowledge skills, and so forth. Sometimes your score is used to calculate another value, such as when you use your Dexterity score to help determine how good you are at avoiding harm using your reflexes (your Reflex saving throw). </p>

<h2>Strength (Str)</h2>
<p>Strength measures sheer muscle power and the ability to apply it. Your Strength score applies to the following: </p>
<ul>
	<li><a href="Combat.php#Damage">Damage dealt by melee and thrown weapon attacks</a>.</li>
	<li><a href="Combat.php#ManeuverBonus">Special combat maneuvers such as grappling, disarming and so on</a>.</li>
	<li><a href="Combat.php#Parry">Defense when parrying, blocking attacks in melee combat</a>.</li>
	<li><a href="Skills.php?Name=Climb">Climb</a>, <a href="Skills.php?Name=Jump">Jump</a>, and <a href="Skills.php?Name=Swim">Swim</a> checks.</li>
	<li><a href="PlayingPhysical.php#Carrying">Your carrying capacity, how much you can lift and carry</a>.</li>
	<li>Strength checks for breaking through doors, smashing things, and other deeds of strength when a specific skill doesn't apply.</li>
</ul>

<h2>Dexterity (Dex)</h2>
<p>Dexterity is a measure of coordination, agility, and manual dexterity. Your Dexterity score applies to the following: </p>
<ul>
	<li><a href="Combat.php#AttackBonus">Attack rolls</a>.</li>
	<li><a href="Combat.php#Dodge">Defense when dodging, evading attacks in combat</a>.</li>
	<li><a href="BasicsSavingThrows.php">Reflex saving throws, for avoiding danger with coordination and quick reflexes</a>.</li>
	<li><a href="CombatInitiative.php">Initiative checks</a>.</li>
	<li><a href="Skills.php?Name=Acrobatics">Acrobatics</a>, <a href="Skills.php?Name=Escape%20Artist">Escape Artist</a>, <a href="Skills.php?Name=Ride">Ride</a>, <a href="Skills.php?Name=Sleight%20of%20Hand">Sleight of Hand</a>, and <a href="Skills.php?Name=Stealth">Stealth</a> checks.</li>
	<li>Dexterity checks for tasks requiring agility and coordination when a specific skill doesn't apply. </li>
</ul>

<h2>Constitution (Con) </h2>
<p>Constitution is a measure of endurance, health, and overall physical toughness. Constitution is important because it affects your hero's ability to resist damage. Your Constitution score applies to the following: </p>
<ul>
	<li><a href="BasicsSavingThrows.php">Toughness saving throws, for resisting damage</a>.</li>
	<li><a href="BasicsSavingThrows.php">Fortitude saving throws, for resisting disease, poison, fatigue, and other effects involving your hero's health</a>.</li>
	<li>Constitution checks for recovering from damage. </li>
</ul>

<h2>Intelligence (Int) </h2>
<p>Intelligence is a measure of reasoning, memory, and quick thinking. Your Intelligence score applies to the following: </p>
<ul>
	<li>Your number of known skills at 1st level (see <a href="Roles.php">Roles</a>).</li>
	<li>The number of skill ranks you gain from successive levels (see <a href="Roles.php">Roles</a>).</li>
	<li><a href="Skills.php?Name=Craft">Craft</a>, <a href="Skills.php?Name=Disable%20Device">Disable Device</a>, <a href="Skills.php?Name=Knowledge">Knowledge</a>, and <a href="Skills.php?Name=Search">Search</a> checks.</li>
	<li>Intelligence checks to solve problems using sheer brainpower when a specific skill doesn't apply. </li>
</ul>

<h2>Wisdom (Wis) </h2>
<p>While Intelligence measures reasoning, Wisdom is a measure of awareness, common sense, intuition, and strength of will. Your Wisdom score applies to the following: </p>
<ul>
	<li><a href="BasicsSavingThrows.php">Will saving throws, for resisting attempts to influence you, whether by mundane or supernatural means</a>. </li>
	<li><a href="Skills.php?Name=Concentration">Concentration</a>, <a href="Skills.php?Name=Medicine">Medicine</a>, <a href="Skills.php?Name=Notice">Notice</a>, <a href="Skills.php?Name=Sense%20Motive">Sense Motive</a>, and <a href="Skills.php?Name=Survival">Survival</a> checks. </li>
	<li>Wisdom checks to resolve matters of intuition when a specific skill doesn't apply. </li>
</ul>

<h2>Charisma (Cha) </h2>
<p>Charisma is a measure of persuasiveness, force of personality, leadership ability, and attractiveness (not necessarily physical). Your Charisma score applies to the following: </p>
<ul>
	<li><a href="Skills.php?Name=Bluff">Bluff</a>, <a href="Skills.php?Name=Diplomacy">Diplomacy</a>, <a href="Skills.php?Name=Disguise">Disguise</a>, <a href="Skills.php?Name=Gather%20Information">Gather Information</a>, <a href="Skills.php?Name=Handle%20Animal">Handle Animal</a>, <a href="Skills.php?Name=Intimidate">Intimidate</a>, and <a href="Skills.php?Name=Perform">Perform</a> checks. </li>
	<li>Charisma checks to use force of personality when a specific skill doesn't apply.</li>
</ul>

<h2>Debilitated Abilities</h2>
<p>There is no limit to how high an ability score can be raised, but there is a limit on how low it can drop. If one of your hero's ability scores drops below -5 for any reason, the score is <em>debilitated</em>. Your hero suffers serious effects, depending on the ability. Debilitated Strength or Dexterity means the hero is helpless and unable to move. Debilitated Constitution means the hero is dying (and suffers a -5 on checks to avoid death). Debilitated Intelligence, Wisdom, or Charisma means the hero is unconscious and incapable of waking until the score is restored to at least -5. </p>

<h2><a name="Nonexistent">Nonexistent Abilities</a></h2>
<p>Some things in DINO-PIRATES OF NINJA ISLAND actually lack a basic ability (having no score in it at all, which is not the same as having a debilitated ability). The effect of lacking a particular ability is as follows: </p>

<h3>Strength </h3>
<p>Any creature capable of physically manipulating other objects has a Strength score. A creature with no Strength is incapable of exerting any physical force, either because it has no physical form (like an incorporeal ghost) or simply can't move (like a tree). The creature automatically fails Strength-based checks. </p>

<h3>Dexterity </h3>
<p>Any creature capable of movement has a Dexterity score. A creature with no Dexterity cannot move (like most plants) or take physical actions and automatically fails Dexterity-based checks. </p>

<h3>Constitution </h3>
<p>Any living creature has a Constitution score. A creature with no Constitution has no physical body (like a ghost) or no living metabolism (like an animated statue or other construct). It is immune to effects requiring Fortitude saving throws unless they work on inanimate objects. The creature always fails Constitution-based checks. Creatures with no Constitution do not recover from damage (since they can't make recovery checks). They must be repaired in some fashion. The same is true of objects. </p>

<h3>Intelligence </h3>
<p>Any creature that can think, learn, or remember has an Intelligence score. A creature with no Intelligence is an automaton, operating on simple instinct or pre-programmed instructions. It is immune to all mental effects and automatically fails Intelligence-based checks. Note that animals have low Intelligence scores (usually -4), but they do have Intelligence. </p>

<h3>Wisdom </h3>
<p>Any creature aware of its environment has a Wisdom score. Anything with no Wisdom also has no Charisma. It is an inanimate object, not a creature. Objects are immune to mind-influencing effects and automatically fail Wisdom-based checks. </p>

<h3>Charisma </h3>
<p>Any creature capable of interacting with others has a Charisma score. Creatures without Charisma cannot interact, are immune to interaction checks, and automatically fail Charisma-based checks. </p>

